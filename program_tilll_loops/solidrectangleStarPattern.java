public class solidrectangleStarPattern {
	public static void main(String[] args) {
		int rows = 5; // Input number of rows
		int coloumns=4;
		for (int i = 1; i <= rows; i++) {
			for (int j = 1; j <= coloumns; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}



